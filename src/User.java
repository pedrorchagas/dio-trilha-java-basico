public class User {
    int numero;
    String nome, agencia;
    double saldo;

    public void definirNumero(int novoNumero) {
        numero = novoNumero;
    }

    public void definirNome(String novoNome) {
        nome = novoNome;
    }

    public void definirAgencia(String novaAgencia) {
        agencia = novaAgencia;
    }

    public void definirSaldo(Double novoSaldo) {
        saldo = novoSaldo;
    }

}
